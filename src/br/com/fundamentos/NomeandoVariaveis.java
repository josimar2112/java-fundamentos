package src.br.com.fundamentos;

public class NomeandoVariaveis {
	public static void main(String[] args) {
		//valido separado por _
		String variavel_separada_com_underline;
		
		//padrao camelCase
		String variavelSeparaComUnderline;
		
		//valido porem estranho
		
		int _numero = 1;
		System.out.println(_numero);
		
		//valdo porem estranho parece PHP
		int $outroNumero = 1;
		System.out.println($outroNumero);
		
	}

}
